func recursiveSort(arr []int, n int, res *[]int) {
	if n == 1 {
		return
	}

	k := getMaxIndex(arr[:n])
	reverse(arr, 0, k)
	*res = append(*res, k + 1)
	reverse(arr, 0, n - 1)
	*res = append(*res, n)

	recursiveSort(arr, n - 1, res)
}

