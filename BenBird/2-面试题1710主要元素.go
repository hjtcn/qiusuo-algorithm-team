func majorityElement_one(nums []int) int {
	m := map[int]int{}
	max_len := len(nums) / 2

	for _, val := range nums {
		if _, ok := m[val]; ok {
			m[val]++
		} else {
			m[val] = 1
		}

		if m[val] > max_len {
			return val
		}
	}

	return -1
}

func majorityElement(nums []int) int {
	major := -1
	count := 0

	for _, num := range nums {
		if count == 0 {
			major = num;
			count++
		} else {
			if major == num {
				count++
			} else {
				count--
			}
		}
	}

	counter := 0
	if count <= 0 {
		return -1
	} else {
		for _, num := range nums {
			if num == major {
				counter++
			}
		}
	}

	if counter > (len(nums) / 2) {
		return major
	}

	return -1
}

