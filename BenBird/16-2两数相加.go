/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func addTwoNumbers(l1 *ListNode, l2 *ListNode) *ListNode {
	l3 := &ListNode{0, nil}
	curr := l3
	carry := 0

	for l1 != nil || l2 != nil {
		num1, num2 := 0, 0

		if l1 != nil {
			num1 = l1.Val
			l1 = l1.Next
		}

		if l2 != nil {
			num2 = l2.Val
			l2 = l2.Next
		}

		sum := num1 + num2 + carry

		if sum >= 10 {
			carry = sum / 10
			sum = sum % 10
		} else {
			carry = 0
		}

		curr.Next = &ListNode{sum, nil}
		curr = curr.Next
	}

	if carry != 0 {
		curr.Next = &ListNode{carry, nil}
	}

	return l3.Next
}
