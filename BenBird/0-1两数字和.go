func twoSum(nums []int, target int) []int {
	new_nums := map[int]int{}
	res := []int{}

	for key, val := range nums {
		if v, ok := new_nums[val]; ok {
			return []int{v, key}
		}

		temp := target - val
		new_nums[temp] = key
	}

	return res
}

